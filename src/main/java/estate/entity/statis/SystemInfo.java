package estate.entity.statis;

/**
 * Created by kangbiao on 15-11-2.
 * 系统信息数据实体类
 */
public class SystemInfo
{
    private Integer appNum;
    private Integer complainNum;
    private Integer repairNum;
    private Integer ownerNum;
    private Integer villageNum;
    private Integer repairManNum;

    public Integer getAppNum()
    {
        return appNum;
    }

    public void setAppNum(Integer appNum)
    {
        this.appNum = appNum;
    }

    public Integer getComplainNum()
    {
        return complainNum;
    }

    public void setComplainNum(Integer complainNum)
    {
        this.complainNum = complainNum;
    }

    public Integer getRepairNum()
    {
        return repairNum;
    }

    public void setRepairNum(Integer repairNum)
    {
        this.repairNum = repairNum;
    }

    public Integer getOwnerNum()
    {
        return ownerNum;
    }

    public void setOwnerNum(Integer ownerNum)
    {
        this.ownerNum = ownerNum;
    }

    public Integer getVillageNum()
    {
        return villageNum;
    }

    public void setVillageNum(Integer villageNum)
    {
        this.villageNum = villageNum;
    }

    public Integer getRepairManNum()
    {
        return repairManNum;
    }

    public void setRepairManNum(Integer repairManNum)
    {
        this.repairManNum = repairManNum;
    }
}
